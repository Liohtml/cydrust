//
// Each pixel below has been pre-decoded to (r5, g6, b5, alpha) tuples.
// We emit LOGICAL Rgb565::new(r5, g6, b5) values; the mipidsi panel is
// configured with ColorOrder::Bgr (matching the reference -DTFT_RGB_ORDER=
// TFT_BGR), so it performs the channel swap in hardware. Do NOT pre-swap here.
//
// Fully/mostly-transparent pixels (alpha < 128) are skipped so the icon
// composites cleanly over the dark background.

