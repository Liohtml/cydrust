/// Shared blit: iterate an 18x18 (r5,g6,b5,alpha) array and draw each
/// non-transparent pixel as a 1x1 rectangle at (x+col, y+row).
fn draw_icon<D: DrawTarget<Color = Rgb565>>(
    d: &mut D,
    px: &[(u8, u8, u8, u8)],
    w: u32,
    x: i32,
    y: i32,
) {
    for (i, &(r, g, b, a)) in px.iter().enumerate() {
        if a < ALPHA_THRESHOLD {
            continue;
        }
        let col = (i as u32 % w) as i32;
        let row = (i as u32 / w) as i32;
        let color = Rgb565::new(r, g, b);
        let _ = Rectangle::new(Point::new(x + col, y + row), Size::new(1, 1))
            .into_styled(PrimitiveStyle::with_fill(color))
            .draw(d);
    }
}

/// Draw the Claude logo with its top-left corner at (x, y).
pub fn draw_claude<D: DrawTarget<Color = Rgb565>>(d: &mut D, x: i32, y: i32) {
    draw_icon(d, &CLAUDE_PX, CLAUDE_W, x, y);
}

/// Draw the Codex logo with its top-left corner at (x, y).
pub fn draw_codex<D: DrawTarget<Color = Rgb565>>(d: &mut D, x: i32, y: i32) {
    draw_icon(d, &CODEX_PX, CODEX_W, x, y);
}
